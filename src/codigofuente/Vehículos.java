package codigofuente;

public class Vehículos {
	
	//Atributos de vehiculos

	
	
	public String Marca;
	public String Modelo;
	public String Color;
	public String Combustible;
	public int Año;
	public int Puertas;
	public int Asientos;
	public String Num_Serie;
	public static int Poliza_Seguro;
	
	//Generar métodos mediante Getters and Setters
	
	
	public String getMarca() {
		return Marca;
	}
	public void setMarca(String marca) {
		Marca = marca;
	}
	public String getModelo() {
		return Modelo;
	}
	public void setModelo(String modelo) {
		Modelo = modelo;
	}
	public String getColor() {
		return Color;
	}
	public void setColor(String color) {
		Color = color;
	}
	public String getCombustible() {
		return Combustible;
	}
	public void setCombustible(String combustible) {
		Combustible = combustible;
	}
	public int getAño() {
		return Año;
	}
	public void setAño(int año) {
		Año = año;
	}
	public int getPuertas() {
		return Puertas;
	}
	public void setPuertas(int puertas) {
		Puertas = puertas;
	}
	public int getAsientos() {
		return Asientos;
	}
	public void setAsientos(int asientos) {
		Asientos = asientos;
	}
	public String getNum_Serie() {
		return Num_Serie;
	}
	public void setNum_Serie(String num_Serie) {
		Num_Serie = num_Serie;
	}
	public int getPoliza_Seguro() {
		return Poliza_Seguro;
	}
	public void setPoliza_Seguro(int poliza_Seguro) {
		Poliza_Seguro = poliza_Seguro;
	}
	


	
	

}
